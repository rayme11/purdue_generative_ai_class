# Addition
x = 10
y = 5
add = lambda a, b: a + b
print("Addition:", add(x, y))  # Output: Addition: 15

# Subtraction
sub = lambda a, b: a - b
print("Subtraction:", sub(x, y))  # Output: Subtraction: 5

# Multiplication
mul = lambda a, b: a * b
print("Multiplication:", mul(x, y))  # Output: Multiplication: 50

# Division
div = lambda a, b: a / b
print("Division:", div(x, y))  # Output: Division: 2.0

# Modulus
mod = lambda a, b: a % b
print("Modulus:", mod(x, y))  # Output: Modulus: 0